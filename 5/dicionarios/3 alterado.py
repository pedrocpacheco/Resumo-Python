eu = {}
# Podemos criar um dic vazio

eu["nome"] = "Thigo"
eu["cidade"] = "Guarulhos"
eu["nascimento"] = "28/07/2005"
# Podemos adicionar chaves no nosso dic vazio

print(eu)
# Resultado -> {'nome': 'Thigo', 'cidade': 'Guarulhos', 'nascimento': '28/07/2005'}

eu["nome"] = "Pedro"
eu["cidade"] = "São Paulo"
# Podemos mudar o valor de itens do dic, passando o nome da chave

print(eu)
# Resultado -> {'nome': 'Pedro', 'cidade': 'São Paulo', 'nascimento': '28/07/2005'}

# LEMBRE-SE -> Dicionario não tem indice! É baseado apenas em Chave - Valor
