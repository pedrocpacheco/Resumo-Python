def funcao_com_argumento(possivel_argumento):
    print(f"Este é o possivel argumento: {possivel_argumento}")

funcao_com_argumento("Pedro!")
# Resposta -> Este é o possivel argumento: Pedro!

def multiplos_argumentos(argumento_1, argumento_2):
    print(f"Podemos ter 1 argumento: {argumento_1}, ou mais: {argumento_2}")

multiplos_argumentos("como este", "mas não é legal ter muitos!")
# Resposta -> Podemos ter 1 argumento: como este, ou mais: mas não é legal ter muitos!